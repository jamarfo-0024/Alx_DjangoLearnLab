# ==========================

def posts_by_tag(request, tag_name):
    tag = get_object_or_404(Tag, name=tag_name)
    posts = tag.post_set.all()

    return render(request, 'blog/post_list.html', {
        'posts': posts,
        'selected_tag': tag
    })


